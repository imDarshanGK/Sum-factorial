def sum(n):
    s=0
    for i in range(1,n+1):
        s=s+i
    return s

def factorial(n):
    s=1
    for i in range(1,n+1):
        s=s*i
    return s

p=sum(3)
q=factorial(6)
print('p=',p,'q=',q)  
                                                                        
    
    
